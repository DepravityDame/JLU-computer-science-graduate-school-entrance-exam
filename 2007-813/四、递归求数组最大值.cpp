int max(int a[],int n) {//函数主体
	if (n ==1) {//递归出口
		return a[0];
	}
	int tmp = max(a + 1, n - 1);//a[0]后元素最大值
	if (a[0] > tmp) {//比较a[0]与max(a[1]->a[100]),返回较大值
		return a[0];
	}
	else return tmp;
}



